import users from './users.js';

const getSortedUniqueSkills = users => {
  return users
    .reduce((allSkills, user) => {
      allSkills.push(...user.skills);
      return allSkills;
    }, [])
    .filter((skill, index, array) => array.indexOf(skill) === index)
    .sort((a, b) => (a > b ? 1 : -1));
};

